								<p style="margin-top: 20px; margin-bottom: 20px">
									<span style="color: rgb(136, 136, 136); font-size: 15px"
										>前言：卖场是否应该统一收银？经销商和卖场经常是各执一词。家具行业操盘手王献永也对此做过专门的探讨，详见文章<a href="https://mp.weixin.qq.com/s?__biz=MzA3NzM0Mjc5OA==&amp;mid=2652035298&amp;idx=1&amp;sn=95faeb34d6269a58d5cf69ab415afdfb&amp;scene=21#wechat_redirect" target="_blank">《实现不了“统一收银”，家居卖场还谈得上管理吗？》</a>“卖场、经销商和消费者应该是夫妻与孩子的关系”，在下面这篇文章，作者谢地对这个问题再度进行了深入思考：</span
									>
								</p>
								<p style="margin-top: 20px; margin-bottom: 20px">前两年写过一遍关于家居卖场统一收银的文章“家居卖场统一收银到底可不可行”（点击阅读原文可见），内容更多的是从方法论角度阐述家居卖场怎么实现统一收银，但并没有从家居卖场的经营逻辑去论证为什么要统一收银，希望这篇文章能把这个问题阐述的清晰一些，给家居卖场的管理者们带来一点小思考。</p>
								<p style="text-align: center"><img class="" data-copyright="0" data-ratio="0.5555555555555556" data-s="300,640" src="./whyunicashier/1.png" data-type="jpeg" data-w="900" style="" /></p>
								<p style="margin-top: 20px; margin-bottom: 20px">根据笔者这些年走访并研究全国家居卖场发展情况统计，目前全国5万平米以上、中高端定位、商场化管理的家居卖场总数应该在1200-1300家，其中全国性连锁家居卖场的门店数量已超过600家，区域性连锁家居卖场门店数量在200家左右，其他都是地方性单体卖场。</p>
								<p style="margin-top: 20px; margin-bottom: 20px">应该说家居卖场发展到如今这个阶段，整体市场已经相对饱和了，尤其是在国内的一二线城市，不仅是饱和，同质化还特别严重。</p>
								<p style="margin-top: 20px; margin-bottom: 20px">无论是全国连锁，区域连锁，亦或是当地单体的家居卖场，租赁模式还是目前的绝对主流，大家都是收租金的，谁也不比谁先进，区别只是经营规模的大小，所处位置的好坏，开业时间的远近，硬件条件的优劣，以及入驻品牌的差异。无论从招商、营销、营运、售后服务、物业以及互联网化改造等方面来说，大部分都没有本质的区别。</p>
								<p style="margin-top: 20px; margin-bottom: 20px">
									<span style="color: rgb(171, 25, 66)"><strong>那么，对于家居卖场来说，能够持续健康经营的核心到底是什么？</strong></span
									>卖场的核心竞争力到底该怎么打造呢？我想这都绕不开一个最基本的问题，就是家居卖场到底是为谁服务的？
								</p>
								<p style="margin-top: 20px; margin-bottom: 20px">其实这个问题的答案是显而易见的，家居卖场当然是为消费者服务的。但是绝大部分家居卖场在实际经营中，是根本掌握不到消费者信息的，即使很多家居卖场已经统一收银了，但是因为还存在商户大量走私单的情况无法避免，导致很多家居卖场的管理者连每年的销售额是多少都不知道。</p>
								<p style="text-align: center"><img class="" data-copyright="0" data-ratio="0.5555555555555556" data-s="300,640" src="./whyunicashier/2.png" data-type="jpeg" data-w="900" style="" /></p>
								<p style="margin-top: 20px; margin-bottom: 20px">一年下来到底有多少消费者在自己的卖场消费了？这些消费者都买了哪些东西？他们什么时候收到货的？他们对购买的产品以及服务是否满意？这些问题家居卖场的管理者们都不知道，谈何为消费者服务呢？</p>
								<p style="margin-top: 20px; margin-bottom: 20px">目前绝大部分家居卖场的经营模式都太简单了，提供场地，招商户入驻收租金；打广告做宣传，吸引消费者来购物。</p>
								<p style="margin-top: 20px; margin-bottom: 20px">真正的订单以及后续的服务发生在商户和消费者之间，商场不介入也不知道怎么介入。这就导致商场在消费者购买家居建材过程中扮演的角色太单一，太容易被取代，所以消费者没有忠诚度，商户也是换来换去，卖场的经营很容易就会碰到瓶颈。</p>
								<p style="margin-top: 20px; margin-bottom: 20px">
									<span style="color: rgb(171, 25, 66)"><strong>这里面核心的原因是这些家居卖场都没弄清楚“商场、商户以及消费者三者应该是什么关系？”</strong></span>
								</p>
								<p style="margin-top: 20px; margin-bottom: 20px">笔者近期在某家居卖场推广统一收银的商户大会上问过现场商户这个问题，有一个商户说卖场和商户应该是“夫妻关系”，我觉得这个比喻很好，那消费者应该就是夫妻共同抚养的“孩子”。</p>
								<p style="text-align: center"><img class="" data-copyright="0" data-ratio="0.625" data-s="300,640" src="./whyunicashier/3.png" data-type="jpeg" data-w="1080" style="" /></p>
								<p style="margin-top: 20px; margin-bottom: 20px">只是绝大部分家居卖场与商户的这种“夫妻关系”都不怎么融洽，互相不信任，严重的还互相打架，商场整天想着怎么让商户交租金涨租金，商户整天想着怎么钻商场的空子，这样的“夫妻关系”，怎么可能抚养好“孩子”呢？</p>
								<p style="margin-top: 20px; margin-bottom: 20px">所以，在笔者看来，<strong>家居卖场和商户应该成为恩爱的“夫妻”，是命运共同体，双方共同的目标和责任是把消费者这个“孩子”抚养好，</strong>双方只有在这个共识下，才能真正实现消费者口碑好、商户生意好、商场经营效益高三者共赢的局面。</p>
								<p style="text-align: center"><img class="" data-copyright="0" data-ratio="0.7137681159420289" data-s="300,640" src="./whyunicashier/0.jpg" data-type="png" data-w="828" style="" /></p>
								<p style="margin-top: 20px; margin-bottom: 20px">而家居卖场通过统一收银分析商户的销售情况，了解消费者购买的产品信息，跟踪每一笔订单的履行情况，获得消费者的评价信息，这就是天经地义理所应当了。</p>
								<p style="margin-top: 20px; margin-bottom: 20px">
									但现实是很多家居卖场推广统一收银的目的并不是我说的这些，<span style="color: rgb(171, 25, 66)"><strong>更多的是为了掌握商户的销售情况好判断是否能涨租金，掌握商户的现金流好规避欠租风险，通过统一收银的资金沉淀获得更多的收益，甚至有的商场会把商户的统一收银款挪作他用，</strong></span
									>这些都不应该是一个持续良性健康经营的家居卖场在推广统一收银时候的目的。
								</p>
								<p style="margin-top: 20px; margin-bottom: 20px">
									虽然笔者不否认统一收银推广的好，可以客观为家居卖场带来上述的一些好处，但是这不应该是目的，<span style="color: rgb(171, 25, 66)"><strong>家居卖场统一收银的目的应该也只能是一个：为了更好的服务消费者。</strong></span>
								</p>
								<p style="margin-top: 20px; margin-bottom: 20px">只有坚守这个前提，并且真正把为消费者服务落到实处，才能真正获得商户的认可，才能解决推广过程中遇到的种种困难。否则花再多的功夫都是白搭，商户抵制、消费者不认可、商场落实不下去就是最终的结局。</p>
								<p style="margin-top: 20px; margin-bottom: 20px">当一个消费者想买喜临门的床垫、芝华仕的沙发、大自然的地板，而这些品牌在她所在城市的每个家居卖场都有，还都是同一个经销商，产品和价格也都一样，这个消费者最终选择到你的卖场购买，因为觉得你的卖场服务好口碑好，买的放心，那么你的卖场就赢啦，你也就有了自己的核心竞争力。</p>
								<p style="margin-top: 20px; margin-bottom: 20px">
									所以，最后总结一点，<span style="color: rgb(171, 25, 66)"><strong>家居卖场是为消费者服务的，占据消费者的心智，消费者口碑好，认可度高，把消费者的口碑作为家居卖场的核心竞争力打造，才是家居卖场经营的根本，这也是家居卖场为什么要统一收银的根本原因。</strong></span>
								</p>
								<p style="margin-bottom: 5px">国内是否有家居卖场统一收银落实得非常好的呢？具体怎么落实统一收银呢？想了解以及交流此话题，请加笔者微信</p>
