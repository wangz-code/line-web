										<section data-tools="135编辑器" data-id="86516" data-color="#ef7060">
											<section style="border-bottom: 1px solid rgb(221, 221, 221); margin-right: auto; margin-bottom: 10px; margin-left: auto; box-sizing: border-box">
												<p data-brushtype="text" style="padding-right: 5px; padding-bottom: 6px; padding-left: 5px; border-bottom: 2px solid rgb(51, 51, 51); display: inline-block; margin-bottom: -1px; line-height: 1.1; font-size: 18px">
													<span style="font-size: 17px; letter-spacing: 2px"><strong>总结：</strong></span>
												</p>
											</section>
										</section>
									</section>
								</section>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">从上面三种模式的阐述中，我们不难判断出，无论哪种模式都已经不适合家居卖场未来管理的需要。</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px">
									<span style="font-size: 16px; color: rgb(171, 25, 66); letter-spacing: 2px"><strong>尤其今年受疫情影响，大部分商户资金周转的压力都非常大，卖场如果还是固守原来的收银模式，必将导致商户更大的抵制，卖场与商户的关系更加紧张。</strong></span>
								</p>
								<p style="margin-top: 15px; margin-bottom: 15px">
									<span style="letter-spacing: 2px"><span style="font-size: 16px">卖场管理者必须根据现实情况进行取舍，采取更加灵活的收银方式。</span></span>
								</p>
								<section data-tools="135编辑器" data-id="93104" data-color="#212122" data-custom="#212122">
									<section style="text-align: center; margin: 10px auto">
										<section style="display: inline-block; color: rgb(63, 63, 63); letter-spacing: 1.5px; border-left: 2px solid rgb(33, 33, 34); padding-right: 10px; padding-left: 10px; text-align: justify; box-sizing: border-box">
											<p>
												<span style="letter-spacing: 2px"><strong>收银3.0模式（2017年-未来）：</strong><strong>电子订单，分布式收银</strong></span>
											</p>
										</section>
									</section>
								</section>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">卖场可以只要数据不要钱！！</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">概述：这种模式下，卖场继续延用统一收银2.0模式的电子订单，要求所有商户部署卖场统一的订单收银系统。</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">商户产生销售时，导购在展厅内登陆卖场的订单收银系统，录入消费者信息、商品明细等，然后在展厅内使用卖场指定银行或支付机构统一为商户部署的智能POS机完成收款，并使用展厅内部署的打印机(没有打印机的商户可以到卖场服务台或者使用自助合同打印机)打印订单以及交款凭证，卖场可以不设集中收银台，或者把收银台和服务台合二为一。</span></p>
								<p style="text-align: center"><img class="rich_pages" data-ratio="0.9370725034199726" data-s="300,640" src="./threecash/16.jpg" data-type="jpeg" data-w="731" style="" /></p>
								<p style="margin-top: 15px; margin-bottom: 15px">
									<span style="letter-spacing: 2px"
										><strong><span style="font-size: 15px; color: rgb(136, 136, 136)">（上图为家居卖场应用智能POS机收款，以及应用自助合同打印机打印收款单和交款单）</span></strong></span
									>
								</p>
								<p style="margin-top: 15px; margin-bottom: 15px">
									<span style="letter-spacing: 2px"
										><strong><span style="font-size: 16px">优点：</span></strong
										><span style="font-size: 16px"></span
									></span>
								</p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">1、卖场如果不想占用商户资金，给商户部署的智能POS可以直接关键商户的账户，货款直接进商户账户，卖场只要掌握数据，商户的推广难度大大降低；</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">2、卖场如果相对强势，对商户的掌控力强，统一收银的基础好，给商户部署的POS机可以继续关联卖场统一收银账户，信息流和资金流都统一归属卖场；</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">3、如果卖场活动期间希望货款进卖场账户，非活动期间货款进商户账户，一样可以支持，非常灵活，这种方式符合绝大部分家居卖场的现状；</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">4、商户不仅可以通过PC端登录系统，也可以通过手机登录系统，使用更加方便；</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">5、卖场基本不用设收银台，节约收银员人力成本；</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">6、商户在展厅内即可完成销售订单的录入以及收款，不用担心消费者交款途中跑单；</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">7、消费者不用走很远到收银台，也不用排队等待交款，体验好；</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">8、卖场可以通过系统看到各种维度的销售数据报表；</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">9、卖场指定银行可以为消费者提供装修贷，为商户提供POS贷等金融服务，为商户和消费者提供更多的增值服务，提升卖场的平台价值。</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px">
									<span style="letter-spacing: 2px"
										><strong><span style="font-size: 16px">缺点：</span></strong
										><span style="font-size: 16px"></span
									></span>
								</p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">1、所有商户部署和学习卖场的收银系统以及智能POS机使用，需要付出一定的学习成本，相对于以上三种收银模式，这个缺点几乎可以忽略不计；</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">2、卖场需要付出一定的管理成本，持续的推广和督促商户学习和习惯统一收银3.0模式的操作，相对于3.0模式带来的好处，这个缺点同样几乎可以忽略不计。</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px">
									<span style="letter-spacing: 2px"
										><strong><span style="font-size: 16px">现状：</span></strong
										><span style="font-size: 16px"></span
									></span>
								</p>
								<p style="margin-top: 15px; margin-bottom: 15px">
									<span style="letter-spacing: 2px"
										><span style="font-size: 16px">1、</span><span style="font-size: 16px; color: rgb(171, 25, 66)"><strong>越来越多的家居卖场在推广统一收银3.0模式，红星美凯龙、居然之家、第六空间、大明宫、吉盛伟邦家具村、烟台嘉禾乐天、昆明华洋家居广场等，对这种模式的认可度越来越高。</strong></span></span
									>
								</p>
								<p style="margin-top: 15px; margin-bottom: 15px">
									<span style="letter-spacing: 2px"><span style="font-size: 16px">2、统一收银3.0模式下，卖场、商户、消费者以及银行，四方可以实现共赢；</span></span>
								</p>
								<section data-tools="135编辑器" data-id="86516">
									<section data-role="paragraph" data-color="#ef7060" style="font-size: 16px">
										<section data-tools="135编辑器" data-id="86516" data-color="#ef7060">
											<section style="border-bottom: 1px solid rgb(221, 221, 221); margin-right: auto; margin-bottom: 10px; margin-left: auto; box-sizing: border-box">
												<p data-brushtype="text" style="padding-right: 5px; padding-bottom: 6px; padding-left: 5px; border-bottom: 2px solid rgb(51, 51, 51); display: inline-block; margin-bottom: -1px; line-height: 1.1; font-size: 18px">
													<span style="font-size: 17px; letter-spacing: 2px"><strong>总结：</strong></span>
												</p>
											</section>
										</section>
									</section>
								</section>

								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">收银3.0模式有效的解决了传统收银模式下信息流和资金流必须合二为一的情况，特别是活动期间和非活动期间可以分情况处理，卖场完全可以根据自身的经营情况灵活运用，对绝大部分卖场来说都不再存在推广的难度。</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px">
									<span style="letter-spacing: 2px"><span style="font-size: 16px">同时卖场通过系统掌握商户的销售数据，了解消费者的消费行为，将为卖场未来调整经营品类和品牌，给消费者提供更好的产品和服务，奠定坚实的数据基础。</span></span>
