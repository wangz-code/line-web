										><span style="font-size: 16px"></span
									></span>
								</p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">1、卖场不用招聘收银员，没有收银员人力成本；</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">2、不占用商户资金，卖场管理难度低；</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px">
									<span style="letter-spacing: 2px"
										><strong><span style="font-size: 16px">缺点：</span></strong
										><span style="font-size: 16px"></span
									></span>
								</p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">1、卖场掌握不了商户的销售情况和消费者信息，没有办法做数据分析；</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px">
									<span style="letter-spacing: 2px"
										><span style="font-size: 16px">2、</span><span style="font-size: 16px; color: rgb(171, 25, 66)"><strong>卖场对商户没有约束力，消费者对卖场没有信任感，消费者和商户出了纠纷，卖场很难处理。</strong></span
										><span style="font-size: 16px">特别是商户一旦撤场甚至逃场，卖场不知道有多少消费者的订单未履约，消费者的权益难以保障。</span></span
									>
								</p>
								<p style="margin-top: 15px; margin-bottom: 15px">
									<span style="letter-spacing: 2px"
										><strong><span style="font-size: 16px">现状：</span></strong
										><span style="font-size: 16px"></span
									></span>
								</p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">1、目前全国还有30%左右的家居卖场，包括一些卖场统一了销售订单格式，但没有设置收银台统一收银的，都可以归类为原始模式。</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px">
									<span style="letter-spacing: 2px"><span style="font-size: 16px">2、卖场采取原始模式的主要原因是商户不配合，卖场没有能力推行统一收银（商户不配合的原因在之前的两篇文章中有详细论述，本文不再重复阐述）。</span></span>
								</p>
								<section data-tools="135编辑器" data-id="93104" data-color="#212122" data-custom="#212122">
