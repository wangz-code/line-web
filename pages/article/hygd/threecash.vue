										><span style="font-size: 16px; color: rgb(171, 25, 66)"><strong>而这种模式的核心就在于：只要数据不要钱。</strong></span></span
									>
								</p>
								<p style="margin-top: 15px; margin-bottom: 15px">
									<span style="letter-spacing: 2px"><span style="font-size: 16px">家居卖场传统的收银模式主要分为以下三种：</span></span>
								</p>
								<section data-tools="135编辑器" data-id="93104" data-color="#212122" data-custom="#212122">
									<section style="text-align: center; margin: 10px auto">
										<section style="display: inline-block; font-size: 16px; color: rgb(63, 63, 63); letter-spacing: 1.5px; border-left: 2px solid rgb(33, 33, 34); padding-right: 10px; padding-left: 10px; text-align: justify; box-sizing: border-box">
											<p>
												<span style="letter-spacing: 2px"
													><strong
														><span style="font-size: 17px">一、原始模式（2006年之前）：<br />商户手工填写订单，商户自己收银&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;&nbsp;</span></strong
													></span
												>
											</p>
										</section>
									</section>
								</section>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="caret-color: red; font-size: 16px; letter-spacing: 2px">概述：这种模式下，卖场没有统一的销售订单模板，商户各用各的订单格式，消费者买了商品后，商户用自己的订单填写相关信息，不用到卖场服务台加盖售后服务章，卖场也不设收银台，商户自己收钱。</span></p>
