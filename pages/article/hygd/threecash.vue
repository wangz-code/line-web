											<p>
												<span style="font-size: 17px; letter-spacing: 2px"
													><strong>二、收银1.0模式（2006年-2014年）：<br />手工订单，收银台集中收银</strong></span
												>
											</p>
										</section>
									</section>
								</section>
								<p style="margin-top: 15px; margin-bottom: 15px">
									<span style="font-size: 16px; letter-spacing: 2px">概述<span style="font-size: 16px; caret-color: red">：这种模式下，卖场设置统一收银台，招聘收银人员，部署简单的收银系统，并且给商户提供统一的销售订单模板。</span></span>
								</p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; caret-color: red; letter-spacing: 2px">消费者买了商品后，商户用卖场的订单填写相关信息，然后带着消费者到收银台，收银员在收银系统里录入订单信息，并且收款，给消费者打印交款凭证，消费者拿着订单和交款凭证到服务台盖卖场售后服务章。</span></p>
								<p style="text-align: center"><img class="rich_pages" data-ratio="0.5336856010568032" data-s="300,640" src="./threecash/14.jpg" data-type="jpeg" data-w="757" style="" /></p>
								<p style="margin-top: 15px; margin-bottom: 15px">
									<span style="letter-spacing: 2px"
										><strong><span style="font-size: 15px; color: rgb(136, 136, 136)">（上图为家居卖场常用的统一的订单模板以及交款凭证，内容都需要导购员自己填写）</span></strong></span
									>
								</p>
								<p style="margin-top: 15px; margin-bottom: 15px">
									<span style="letter-spacing: 2px"
										><strong><span style="font-size: 16px">优点：</span></strong
										><span style="font-size: 16px"></span
									></span>
								</p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">1、统一订单格式和收银台交款增加消费者对卖场的信任感；</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">2、卖场代收货款可以增加对商户的管控和约束力；</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">3、卖场一定程度上掌握商户销售情况以及消费者信息，便于数据分析；</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">4、卖场通过收银款代扣商户租金等费用，规避欠租风险。</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px">
									<span style="letter-spacing: 2px"
										><strong><span style="font-size: 16px">缺点：</span></strong
										><span style="font-size: 16px"></span
									></span>
								</p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">1、商户对统一收银比较排斥，卖场推广难度大，管理成本高；</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">2、非活动期间，收银台开的少，消费者交款不方便；活动期间，消费者需要排队交款，体验差；&nbsp;</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">3、商户担心消费者去交款途中，有可能跑单；</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">4、手写订单，字迹潦草不清、影响消费者对商户和卖场的好感度；</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">5、.卖场需要招聘多名收银人员，大部分时候很闲，人力成本高。</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px">
									<span style="letter-spacing: 2px"
										><strong><span style="font-size: 16px">现状：</span></strong
										><span style="font-size: 16px"></span
									></span>
								</p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">1、目前全国40%左右的家居卖场，基本上都采取了统一收银1.0模式；</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px">
									<span style="letter-spacing: 2px"
										><span style="font-size: 16px">2、</span><strong><span style="font-size: 16px; color: rgb(171, 25, 66)">大部分家居卖场只能做到活动期间统一收银，</span></strong
										><span style="font-size: 16px">因为卖场有补贴，商户和消费者都愿意交款到卖场，享受活动优惠；</span></span
									>
								</p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">3、非活动期间卖场没有有效的措施保证商户和消费者参加统一收银，统一收银率低；</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px">
									<span style="letter-spacing: 2px"
										><span style="font-size: 16px">4、</span><span style="font-size: 16px; color: rgb(171, 25, 66)"><strong>收银系统应用程度低，只满足简单的订单和收款功能，没有真正把系统的价值发挥出来。</strong></span></span
									>
								</p>
								<section data-tools="135编辑器" data-id="93104" data-color="#212122" data-custom="#212122">
									<section style="text-align: center; margin: 10px auto">
										<section style="display: inline-block; font-size: 16px; color: rgb(63, 63, 63); letter-spacing: 1.5px; border-left: 2px solid rgb(33, 33, 34); padding-right: 10px; padding-left: 10px; text-align: justify; box-sizing: border-box">
											<p>
												<span style="font-size: 17px; letter-spacing: 2px"
													><strong>三、收银2.0模式（2014年-2018年）：<br />电子订单，收银台集中收银</strong></span
												>
											</p>
										</section>
									</section>
								</section>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="caret-color: red; font-size: 16px; letter-spacing: 2px">概述：这种模式下，卖场取消手写订单，要求所有商户部署卖场统一的订单收银系统，商户产生销售时，导购在展厅内登陆卖场的订单收银系统，录入消费者信息、商品明细等，然后带着消费者到收银台，收银员从系统中调出该导购录入的订单，完成收款、打印交款凭证以及销售订单，再到服务台加盖售后服务章。</span></p>
								<p style="text-align: center"><img class="rich_pages" data-ratio="0.7435197817189632" data-s="300,640" src="./threecash/15.jpg" data-type="jpeg" data-w="733" style="" /></p>
								<p>
									<span style="letter-spacing: 2px"
										><strong><span style="font-size: 15px; color: rgb(136, 136, 136)">（上图为家居卖场电子订单的样例，系统录入后直接打印出来，对比原来的手写订单进步很多）</span></strong></span
									>
								</p>
								<p style="margin-top: 15px; margin-bottom: 15px">
									<span style="letter-spacing: 2px"
										><strong><span style="font-size: 16px">优点：</span></strong
										><span style="font-size: 16px"></span
									></span>
								</p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">1、电子订单取代了手工订单，品牌和卖场的形象大大提升，消费者认可度高；</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">2、收银员不用录入订单信息，收银效率大大加快。</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px">
									<span style="letter-spacing: 2px"
										><strong><span style="font-size: 16px">缺点：</span></strong></span
									>
								</p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">1、只是把手工订单变成了电子订单，集中收银台收银的缺点依然继续存在；</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">2、需要所有商户导购学习订单系统的使用，有一定的推广难度，对卖场的管理要求较高；</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">3、商户端系统只支持PC端，对商户的硬件要求较高。</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px">
									<span style="letter-spacing: 2px"
										><strong><span style="font-size: 16px">现状：</span></strong
										><span style="font-size: 16px"></span
									></span>
								</p>
								<p style="margin-top: 15px; margin-bottom: 15px"><span style="font-size: 16px; letter-spacing: 2px">1、统一收银2.0模式是一种过渡模式，目前只有不到10%左右的家居卖场在采取这种模式；</span></p>
								<p style="margin-top: 15px; margin-bottom: 15px">
									<span style="letter-spacing: 2px"><span style="font-size: 16px">2、适合相对强势的家居卖场，对商户有相当强的管控力。</span></span>
								</p>
